	/**
	 * You can only get a thousand records, change the value to get the other thousand.
	 */
	public void parseToJavaFile()
	{
		for (SystemMessageId sm : SystemMessageId.values())
		{
			StringBuilder sb = new StringBuilder();
			if (messages.get(sm.getId()) != null && sm.getId() <= 1000)
			{
				sb.append("/**\n");
				sb.append(" * ID: ");
				sb.append(sm.getId());
				sb.append("<br>\n");
				sb.append(" * Message: ");
				sb.append(messages.get(sm.getId()));
				sb.append("\n");
				sb.append(" */\n");
				sb.append(sm);
				sb.append("(");
				sb.append(sm.getId());
				sb.append("),\n");
				
				System.out.println(sb.toString());
			}
		}
	}
